// ── 洗牌式随机：遍历完全部 tips 之后才会重复 ──
let _tipDeck = [];
let _tipDeckIdx = 0;

function _shuffleArray(arr) {
    for (let i = arr.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [arr[i], arr[j]] = [arr[j], arr[i]];
    }
}

function _ensureTipDeck() {
    if (_tipDeck.length === 0 || _tipDeckIdx >= _tipDeck.length) {
        // 重新洗牌：创建索引数组并打乱
        _tipDeck = Array.from({ length: WAITING_TIPS.length }, (_, i) => i);
        _shuffleArray(_tipDeck);
        _tipDeckIdx = 0;
    }
}

function getRandomTip() {
    _ensureTipDeck();
    const idx = _tipDeck[_tipDeckIdx++];
    return WAITING_TIPS[idx];
}
